<?php
string $variable = "Hello";                        // string
var_dump($variable);

$variable = 42;                             // int
var_dump($variable);

$variable = 3.14;                           // float
var_dump($variable);

$variable = true;                           // bool
var_dump($variable);

$variable = [true, 2, "3", 4 => [5, 6, 7]]; // array
var_dump($variable);

$variable = null;                           // null
var_dump($variable);
